<h2><a name="intro" class="anch">Introduction</a></h2>
<p><b>Rome</b> is a grand-strategy roleplaying game. You play a character of your own choosing and you are free to develop your character in this world as you see fit. In the game you play a character who may be part of a faction. The goal of all factions is to dominate the known world.</p>

<h2><a name="basics" class="anch">Registration</a></h2>
<p>You need to register before you can play this game.<p>
<H2><a name="start" class="anch">Getting Started</a></H2>
<p>Once you are registered, you need to fill in further information about your player character.</p>
<p>You have to choose whether you want your character, to be male or female.</p>
<p>You also need to choose a name for your character, a skill, an item, and a portrait. The available portraits depend upon on you whether you chose to be a male or female.</p>
<p>You have to decide in which settlement to place your home and a faction.</p>
<p>From then on you can play the game.</p>
